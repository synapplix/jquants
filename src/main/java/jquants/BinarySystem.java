package jquants;

/**
 * Singleton defining Metric System multipliers
 *
 * @author  Mathias Braeu
 * @since   1.0
 *
 */
public class BinarySystem {
  public static final double Kilo = 1024d;
  public static final double Mega = 1024d * Kilo;
  public static final double Giga = 1024d * Mega;
  public static final double Tera = 1024d * Giga;
  public static final double Peta = 1024d * Tera;
  public static final double Exa = 1024d * Peta;
  public static final double Zetta = 1024d * Exa;
  public static final double Yotta = 1024d * Zetta;
}